"十一",
"南宋词人，白石有格而无情，剑南有气而乏韵。其堪与北宋人颉颃者，唯一幼安耳。近人祖南宋而祧北宋，以南宋之词可学，北宋不可学也。学南宋者，不祖白石，则祖梦窗，以白石、梦窗可学，幼安不可学也。学幼安者，率祖其粗犷滑稽，以其粗犷、滑稽处可学，佳处不可学也。同时白石、龙洲学幼安之作且如此，况他人乎？其实幼安词之佳者，如《摸鱼儿》《贺新郎送茂嘉》《青玉案元夕》《祝英台近》等，俊伟幽咽固独有千古，其他豪放之处亦有“横素波、干青云”之概，宁梦窗辈龌龊小生所可语耶？",
