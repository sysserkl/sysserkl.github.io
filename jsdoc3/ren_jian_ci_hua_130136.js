"六十六",
"词家时代之说，盛于国初。竹垞谓词至北宋而大，至南宋而深。后此词人，群奉其说。然其中亦非无具眼者。周保绪曰：“南宋下不犯北宋拙率之病，高不到北宋浑涵之诣。”又曰：“北宋词多就景叙情，故珠圆玉润，四照玲珑。至稼轩、白石，一变而为即事叙景，使深者反浅，曲者反直。”潘四农德舆曰：“词滥觞于唐，畅于五代，而意格之闳深曲挚则莫盛于北宋。词之有北宋，犹诗之有盛唐。至南宋则稍衰矣”。刘融斋熙载曰：“北宋词用密亦疏、用隐亦亮、用沈亦快、用细亦阔、用精亦浑。南宋只是掉转过来。”可知此事自有公论。虽止庵词颇浅薄，潘、刘尤甚。然其推尊北宋，则与明季云间诸公同一卓识，不可废也。",
