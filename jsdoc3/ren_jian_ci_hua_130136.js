"二十四",
"余填词不喜作长调，尤不喜用人韵。偶尔游戏，作《水龙吟》咏杨花用质夫、东坡倡和韵，作《齐天乐》咏蟋蟀用白石韵，皆有与晋代兴之意。余之所长殊不在是，世之君子宁以他词称我。",
"二十五",
"余友沈昕伯纺自巴黎寄余《蝶恋花》一阕云：“帘外东风随燕到。春色东来，循我来时道。一霎围场生绿草，归迟却怨春来早。锦绣一城春水绕。庭院笙歌，行乐多年少。著意来开孤客抱，不知名字闲花鸟。”此词当在晏氏父子间，南宋人不能道也。",
"二十六",
"樊抗夫谓余词如《浣溪沙》之“天末同云”、《蝶恋花》之“昨夜梦中”、“百尺高楼”、“春到临春”等阕，凿空而道，开词家未有之境。余自谓才不若古人，但于力争第一义处，古人亦不如我用意耳。",
"二十七",
"东坡杨花词和韵而似元唱，章质夫词元唱而似和韵。才之不可强也如是。",
"二十八",
"叔本华曰：“抒情诗，少年之作也。叙事诗及戏曲，壮年之作也。”余谓：抒情诗，国民幼稚时代之作也。叙事诗，国民盛壮时代之作也。故曲则古不如今。（元曲诚多天籁，然其思想之陋劣，布置之粗笨，千篇一律令人喷饭。至本朝之《桃花扇》《长生殿》诸传奇，则进矣）。词则今不如古。盖一则以布局为主，一则须伫兴而成故也。",
"二十九",
"北宋名家以方回为最次，其词如历下、新城之诗，非不华瞻，惜少真味。至宋末诸家，仅可譬之腐烂制艺，乃诸家之享重名者且数百年，始知世之幸人不独曹蜍、李志也。",
"三十",
"散文易学而难工，骈文难学而易工。近体诗易学而难工，古体诗难学而易工。小令易学而难工，长调难学而易工。",
"三十一",
"词以境界为最上。有境界则自成高格，自有名句。五代北宋之词所以独绝者在此。",
"三十二",
"有造境，有写境。此理想与写实二派之所由分。然二者偏难区别。因大诗人所造之境，必合乎自然，所写之境，必邻于理想故也。",
"三十三",
"有有我之境，有无我之境。“泪眼问花花不语，乱红飞过秋千去”，“可堪孤馆闭春寒，杜鹃声里斜阳暮”，有我之境也。“采菊东篱下，悠然见南山”，“寒波澹澹起，白鸟悠悠下”，无我之境也。有我之境，物皆著我之色彩。无我之境，不知何者为我，何者为物。此即主观诗与客观诗之所由分也。古人为词，写有我之境者为多，然非不能写无我之境，此在豪杰之士能自树立耳。",
"三十四",
"古诗云:“谁能思不歌？谁能饥不食？”诗词者，物之不得其平而鸣者也。故“欢愉之辞难工，愁苦之言易巧。”",
"三十五",
"境非独谓景物也，感情亦人心中之境界。故能写真景物、真感情者谓之有境界，否则谓之无境界。",
"三十六",
"无我之境，人唯于静中得之。有我之境，于由动之静时得之。故一优美，一宏壮也。",
"三十七",
"自然中之物，互相关系，互相限制，故不能有完全之美。然其写之于文学中也，必遗其关系、限制之处。故虽写实家亦理想家也。又虽如何虚构之境，其材料必求之于自然，而其构造亦必从自然之法则。故虽理想家，亦写实家也。",
"三十八",
"社会上之习惯，杀许多之善人。文学上之习惯，杀许多之天才。",
"三十九",
"诗之三百篇、十九首，词之五代北宋，皆无题也。非无题也，诗词中之意不能以题尽之也。自《花庵》《草堂》每调立题，并古人无题之词亦为之作题，其可笑孰甚。诗词之题目本为自然及人生。自古人误以为美刺投赠咏史怀古之用，题目既误，诗亦自不能佳。后人才不及古人，见古名、大家亦有此等作，遂遗其独到之处而专学此字.不复知诗词之本意。于是豪杰之士不得不变其体格，如楚辞、汉之五言诗、唐五代北宋之词皆是也。故此等文学皆无题。诗有题而诗亡，词有题而词亡。然中材之士鲜能知此而自振拔者矣。",
"四十",
"冯梦华《宋六十一家词选序》谓:“淮海、小山古之伤心人也。其淡语皆有味，浅语皆有致。”余谓此唯淮海足以当之。小山矜贵有余，但稍胜方回耳。古人以秦七、黄九或小晏、秦郎并称，不图老子乃与韩非同传。",
"四十一",
"人能于诗词中不为美刺投赠怀古咏史之篇，不使隶事之句，不用装饰之字，则于此道已过半矣。",
"四十二",
"以《长恨歌》之壮采，而所隶之事，只“小玉”、“双成”四字，才有余也。梅村歌行，则非隶事不可。白、吴优劣即于此见。此不独作诗为然，填词家亦不可不知也。",
"四十三",
"词之为体，要眇宜修。能言诗之所不能言，而不能尽言诗之所能言。诗之境阔，词之言长。",
"四十四",
"“明月照积雪”、“大江流日夜”、“澄江净如练”、“山气日夕佳”、“落日照大旗”、“中天悬明月”、“大漠孤烟直，长河落日圆”，此等境界可谓千古壮语。求之于词，则纳兰容若塞上之作，如《长相思》之“夜深千帐灯”，《如梦令》之“万帐穹庐人醉，星影摇摇欲坠”差近之。",
"四十五",
"言气质，言神韵，不如言境界。有境界，本也。气质、神韵，末也。有境界而二者随之矣。",
"四十六",
"“红杏枝头春意闹”，著一“闹”字而境界全出。“云破月来花弄影”，著一“弄”字而境界全出矣。",
"四十七",
"“西风吹渭水，落叶满长安。”美成以之入词。白仁甫以之入曲。此借古人之境界为我之境界者也。然非自有境界，古人亦不为我用。",
"四十八",
"境界有大小，然不以是而分高下。“细雨鱼儿出，微风燕子斜”，何遽不若“落日照大旗，马鸣风萧萧。”、“宝帘闲挂小银钩”，何遽不若“雾失楼台，月迷津渡”也。",
"四十九",
"昔人论诗词有景语、情语之别。不知一切景语皆情语也。",
"五十",
