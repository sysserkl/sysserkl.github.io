"十五",
"双声叠韵之论盛于六朝，唐人犹多用之。至宋以后则渐不讲，并不知二者为何物。乾嘉间吾乡周松霭先生春著《杜诗双声叠韵谱括略》，正千余年之误，可谓有功文苑者矣。其言曰：“两字同母谓之双声，两字同韵谓之叠韵。”余按：用今日各国文法通用之语表之，则两字同一子音者谓之双声。（如《南史。羊元保传》之：“官家恨狭，更广八分”，官、家、更、广四字皆从K得声。《洛阳伽蓝记》之“狞奴慢骂”，狞、奴、二字皆从n得声，慢、骂二字皆从m得声是也。）两字同一母音者，谓之叠韵，（如梁武帝之“后牖有朽柳”，后、牖、有三字双声而兼叠韵，有、朽、柳三字其母音皆为u。刘孝绰之“梁皇长康强”，梁、长、强三字其母音皆为iang也。）自李淑《诗苑》伪造沈约之说，以双声叠韵为诗中八病之二，后世诗家多废而不讲，亦不复用之于词。余谓苟于词之荡漾处用叠韵，促节处用双声，则其铿锵可诵必有过于前人者。惜世之专讲音律者，尚未悟此也。",
"十六",
"昔人但知双声之不拘四声，不知叠韵亦不拘平、上、去三声。凡字之同母者，虽平仄有殊皆叠韵也。",
"十七",
"诗至唐中叶以后，殆为羔雁之具矣。故五代北宋之诗，佳者绝少，而词则为其极盛时代。即诗词兼擅如永叔、少游者，亦词胜于诗远甚。以其写之于诗者，不若写之于词者之真也。至南宋以后，词亦为羔雁之具，而词亦替矣。此亦文学升降之一关键也。",
