或许是对错过“历史列车”的愤恨，以及驰骋于“历史”原野的渴望。对他们来说，“承认”不仅仅意味着权利，还意味着确立权利的权力。这种创造历史的英雄情结，或许将终结“历史的终结”，使其“从头再来”。
