自由民主国家存在很多“问题”（包括不平等这个问题），但这些“问题”并不构成根本性的“矛盾”。之所以不构成根本矛盾，是因为自由民主制作为一个具有内部纠错功能的机制，能够在制度内部解决这些“问题”，无需诉诸制度更替本身。
其他政治制度则由于权力结构的缺陷，缺乏如此之有弹性的自我调校空间，而这正是它们一一衰败的原因。
自由民主国家代表着“奴隶”的绝对胜利。
自由民主制未必扼杀人的创造力、勇气和技艺，只是将过去往往由出身决定的机械精英主义替换成了现在更与能力相联系的有机精英主义。
