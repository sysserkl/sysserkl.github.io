如果人们关心的仅仅是满足欲望和理性的“经济”指标，也许自由民主制并非最佳选择：“如果人们只有欲望和理性，那他们就会满足于市场导向的威权国家，比如佛朗哥的西班牙，以及军人统治下的韩国或巴西。可是，他们对于自身的自我价值有一种充满激情的自豪，正是这种充满激情的自豪促使他们向往民主，因为民主政府待他们如成人而非孩童，并且承认他们作为自由个体的自主权。”
人性中普遍的寻求“承认”的心理需求
人的灵魂有三个部分：欲望、理性和激情。
严格等级制下的“承认”是不能令人满足的。首先，它不是相互的
即使是奴隶对奴隶主、臣民对君主、农奴对贵族的承认，由于它建立在强制和依赖的基础上，也是不能令人满足的。武力威胁或者利益收买下的“爱戴”并没有自发基础上的“爱戴”来得甜蜜——只有对方是具有伦理选择能力的自由人，其“承认”才真正给我们带来快感和满足。这合乎我们的经验感受——一个美丽姑娘真正爱上了某个男人“这个人”，而不是被他用枪胁迫或者用钱收买，她的爱才真正令这个男人感到由衷的满足；如果所有学生都自主选择留在一个老师的课堂上聚精会神听讲，而不是因为老师要点名、老师可能给低分才留下来，这个老师获得的“承认”才真正令其满足。正是在这个意义上，不仅社会的弱者，而且社会的强者，也需要通过自由民主这种社会形态来得到最有意义的“承认”——唯有赋予他人自由与权利，强者才能从他们那里得到有意义的承认。
黑格尔在1806年耶拿战役之后，就宣布了“历史的终结”。
人类普遍的、相互的承认形式已经被找到并开始通过强力传播。
